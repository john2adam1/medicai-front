type TKeys = keyof typeof translations['uz'];

interface I18nState {
    lang: Language;
    setLang: (lang: Language) => void;
    // This cannot be safely persisted via zustand action directly if we return a functional closure, 
    // but we can just use a helper function directly on the exported object.
}

export const useI18n = create<I18nState>()(
    persist(
        (set) => ({
            lang: 'uz',
            setLang: (lang) => set({ lang }),
        }),
        { name: 'medicai_language' }
    )
);

export function useTranslation() {
    const lang = useI18n(s => s.lang);
    return {
        t: (key: TKeys) => translations[lang][key] || key,
        lang
    }
}
